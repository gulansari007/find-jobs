class Job {
  final int id;
  final String title;
  final String company;
  final String location;
  final String type;
  final String salary;
  final String logoPath;

  Job({
    required this.id,
    required this.title,
    required this.company,
    required this.location,
    required this.type,
    required this.salary,
    required this.logoPath,
  });
}
